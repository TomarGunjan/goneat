package com.goneat.goneat.ui.mode.response;

public class BookedDetails {
	private String bookingId;
	private String startTime;
	private Integer numberOdGuests;
	public String getBookingId() {
		return bookingId;
	}
	public void setBookingId(String bookingId) {
		this.bookingId = bookingId;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public Integer getNumberOdGuests() {
		return numberOdGuests;
	}
	public void setNumberOdGuests(Integer numberOdGuests) {
		this.numberOdGuests = numberOdGuests;
	}
	
}
